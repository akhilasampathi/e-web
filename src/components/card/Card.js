import React, { useState } from 'react';
import './Card.css';
import { Link, useNavigate } from "react-router-dom";
import SERVER_URL from '../../config';
const Card = () => {
  const [isOpen, setIsOpen] = useState(false);
  const navigate = useNavigate();
  const [email, setEmail] = useState('');
  const [otp, setOTP] = useState('');
  const [error, setError] = useState('');
  const handleLogin = async () => {
    try {
      const response = await fetch(`${SERVER_URL}/user/login`, {
        method: 'POST',
        body: JSON.stringify({ email, otp }),
        headers: {
          'Content-Type': 'application/json'
        }
      });
      if (response.ok) {
        
        // OTP verification successful, navigate to home page
        navigate('/about');
      } else {
        // OTP verification failed
        setError('Invalid OTP. Please try again.');
      }
    } catch (error) {
      // Error occurred while verifying OTP
      setError('An error occurred while logging in. Please try again.');
    }
  };
  const openPopup = () => {
    setIsOpen(true);
  };
  const closePopup = () => {
    setIsOpen(false);
  };
  return (
    <div className="popup-container">
      <h2 onClick={openPopup}>Login</h2>
      {isOpen && (
        <div className="popup">
          <div className="popup-content">
          <form onSubmit={(e) => e.preventDefault()}>
            <h2>Login to your Gallaxy Account</h2>
          <div className='element'>
            <label>Email:</label>
            <input type="email" value={email} onChange={(e) => setEmail(e.target.value)} />
          </div>
              <div className='element'>
                <label>OTP:</label>
                <input type="text" value={otp} onChange={(e) => setOTP(e.target.value)} />
              </div>
              <button className='btnl' onClick={handleLogin}>Login</button><br/>
              {error && <p className="error">{error}</p>}
             <h4> Don’t have a Gallaxy Account yet?<Link to="/Registerform" >Sign Up Now</Link></h4>
             <h2><Link to="/Logout" >Logout</Link></h2>
        </form>
            <button  className='btnn' onClick={closePopup}>Close</button>
          </div>
        </div>
      )}
       
    </div>
  );
};
export default Card;
